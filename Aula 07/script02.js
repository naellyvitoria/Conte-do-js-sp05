// Exemplo inicial laço de repetição for
for (let contadora = 0; contadora < 10; contadora++) {
  console.log(contadora);
}

console.log("");

// Exemplo inicial laço de repetição while
let contadora = 0;

while (contadora < 10) {
  console.log(contadora);
  contadora++;
}

console.log("");

// Exemplo inicial laço de repetição do while
let contadora2 = 0;

do {
  console.log(contadora2);
  contadora2++;
} 
while (contadora2 < 10);