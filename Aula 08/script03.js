// Exercício 2: Realize a operação de subtração com números inteiros informados pelo usuário e depois retorne o resultado para ele.

alert("Subtração");

let valor1 = parseInt(prompt("Informe um primeiro valor inteiro para realizar a operação de subtração: "));

let valor2 = parseInt(prompt("Informe um segundo valor inteiro para realizar a operação de subtração: "));

let resultadoSubtracao = valor1 - valor2;

alert("O resultado da subtração é: " + resultadoSubtracao);