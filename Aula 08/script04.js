// Exercício 3: Realize a operação de multiplicação com números inteiros informados pelo usuário e depois retorne o resultado para ele.

alert("Multiplicação");

let valor1 = parseInt(prompt("Informe um primeiro valor inteiro para realizar a operação de multiplicação: "));

let valor2 = parseInt(prompt("Informe um segundo valor inteiro para realizar a operação de multiplicação: "));

let resultadoMultiplicacao = valor1 * valor2;

alert("O resultado da multiplicação é: " + resultadoMultiplicacao);