// Exercício 5: Calcule a área de um retângulo de acordo com os dados informados pelo usuário.

alert("Área de retângulo");

let baseRetangulo = parseFloat(prompt("Informe o valor da base do retângulo: "));

let alturaRetangulo = parseFloat(prompt("Informe o valor da altura do retângulo: "));

let areaRetangulo = baseRetangulo * alturaRetangulo;

alert("A área total do retângulo é: " + areaRetangulo);